package com;

import java.io.Serializable;
import javax.persistence.Basic;
import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.Id;
import javax.persistence.NamedQueries;
import javax.persistence.NamedQuery;
import javax.persistence.Table;
import javax.validation.constraints.NotNull;
import javax.validation.constraints.Size;
import javax.xml.bind.annotation.XmlRootElement;

/**
 *
 * @author it354f701
 */
@Entity
@Table(name = "IT354_sanket.LOGININFO")
@XmlRootElement
@NamedQueries({
    @NamedQuery(name = "Logininfo.findAll", query = "SELECT l FROM Logininfo l"),
    @NamedQuery(name = "Logininfo.findByEmail", query = "SELECT l FROM Logininfo l WHERE l.email = :email"),
    @NamedQuery(name = "Logininfo.findByFirstName", query = "SELECT l FROM Logininfo l WHERE l.firstName = :firstName"),
    @NamedQuery(name = "Logininfo.findByLastName", query = "SELECT l FROM Logininfo l WHERE l.lastName = :lastName"),
    @NamedQuery(name = "Logininfo.findByPassword", query = "SELECT l FROM Logininfo l WHERE l.password = :password")})
public class Logininfo implements Serializable {
    private static final long serialVersionUID = 1L;
    // @Pattern(regexp="[a-z0-9!#$%&'*+/=?^_`{|}~-]+(?:\\.[a-z0-9!#$%&'*+/=?^_`{|}~-]+)*@(?:[a-z0-9](?:[a-z0-9-]*[a-z0-9])?\\.)+[a-z0-9](?:[a-z0-9-]*[a-z0-9])?", message="Invalid email")//if the field contains email address consider using this annotation to enforce field validation
    @Id
    @Basic(optional = false)
    @NotNull
    @Size(min = 1, max = 50)
    @Column(name = "EMAIL")
    private String email;
    @Size(max = 20)
    @Column(name = "FIRST_NAME")
    private String firstName;
    @Size(max = 20)
    @Column(name = "LAST_NAME")
    private String lastName;
    @Size(max = 20)
    @Column(name = "PASSWORD")
    private String password;

    public Logininfo() {
    }

    public Logininfo(String email) {
        this.email = email;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getFirstName() {
        return firstName;
    }

    public void setFirstName(String firstName) {
        this.firstName = firstName;
    }

    public String getLastName() {
        return lastName;
    }

    public void setLastName(String lastName) {
        this.lastName = lastName;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    @Override
    public int hashCode() {
        int hash = 0;
        hash += (email != null ? email.hashCode() : 0);
        return hash;
    }

    @Override
    public boolean equals(Object object) {
        // TODO: Warning - this method won't work in the case the id fields are not set
        if (!(object instanceof Logininfo)) {
            return false;
        }
        Logininfo other = (Logininfo) object;
        if ((this.email == null && other.email != null) || (this.email != null && !this.email.equals(other.email))) {
            return false;
        }
        return true;
    }

    @Override
    public String toString() {
        return "com.Logininfo[ email=" + email + " ]";
    }
    
}
